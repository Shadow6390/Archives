    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try
        {
            plotGraph.clearPoints();
            double min=Double.parseDouble(tempExtMinField.getText()),max=Double.parseDouble(tempExtMaxField.getText());
            int pointNumber=30;
            plotGraph.setXScale(12);
            plotGraph.setYScale(1);
            double maxVal = 2d*Math.PI;
            double iteration = maxVal/((double)pointNumber);
            double y;
            for (double i=0;i<maxVal;i+=iteration)
            {
                y=(min+max)*0.5+(0.5*(max-min)*Math.sin(i));
                plotGraph.addPoint(new PointDouble(i,y));
            }
            repaint();
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this,Simulacao.getFrasePelaChave("uc2.number.exception"),"",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

