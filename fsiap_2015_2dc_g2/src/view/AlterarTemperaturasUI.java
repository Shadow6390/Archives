    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        // TODO add your handling code here:
        try
        {
            double tempInt = Double.parseDouble(tempIntField.getText());
            double tempIntAlvo = Double.parseDouble(tempIntAlvoField.getText());
            double tempExt = Double.parseDouble(tempIntField.getText());
            boolean hasVariation = radioButton.isSelected();
            if (hasVariation)
            {
                double tempExtMin = Double.parseDouble(tempExtMinField.getText());
                double tempExtMax = Double.parseDouble(tempExtMaxField.getText());
                controller.setTemperaturaExteriorMinima(tempExtMin);
                controller.setTemperaturaExteriorMaxima(tempExtMax);
            }
            controller.setTemperaturaInterior(tempInt);
            controller.setTemperaturaInteriorAlvo(tempIntAlvo);
            controller.setTemperaturaExterior(tempExt);
            controller.setHasTemperaturaVariation(hasVariation);
            if (controller.valida())
            {
                controller.alterarSala();
                JOptionPane.showMessageDialog(this,Simulacao.getFrasePelaChave("uc2.operation.success"),"",JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(this,Simulacao.getFrasePelaChave("uc2.operation.failure"),"",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this,Simulacao.getFrasePelaChave("uc2.number.exception"),"",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_acceptButtonActionPerformed

