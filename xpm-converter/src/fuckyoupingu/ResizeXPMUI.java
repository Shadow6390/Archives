    public ResizeXPMUI(java.awt.Frame parent, boolean modal,File xpm) {
        super(parent, modal);
        initComponents();
        InputVerifier numberVerifier = new InputVerifier() {

            @Override
            public boolean verify(JComponent input) {
                boolean result = false;
                if (input.getClass().equals(JTextField.class))
                {
                    JTextField component = (JTextField) input;
                    result = component.getText().matches("[0-9]*");
                }
                if (!result)
                {
                    SwingUtilities.invokeLater(() -> {
                        JOptionPane.showMessageDialog(null,"Value must be a whole number!","ERROR",JOptionPane.ERROR_MESSAGE);
                    });
                    input.setBackground(Color.red);
                }
                else
                {
                    input.setBackground(Color.white);
                }
                return result;
            }
        };
        widthField.setInputVerifier(numberVerifier);
        heightField.setInputVerifier(numberVerifier);
        this.xpm=xpm;
    }

