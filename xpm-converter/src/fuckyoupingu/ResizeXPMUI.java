    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try 
        {
            File file = xpm;
            int newWidth = Integer.parseInt(widthField.getText());
            int newHeight = Integer.parseInt(heightField.getText());

            XPMConverter xpm = new XPMConverter();
            String name = file.getName();
            name = name.substring(0,name.indexOf("."))+"_resized_"+newWidth+"x"+newHeight;
            xpm.setName(name);
            if (xpm.save(XPMConverter.resizeXPM(XPMConverter.getXPM(file), newWidth,newHeight), file.getParent(), true))
            {
                JOptionPane.showMessageDialog(rootPane,"Resize successful. File saved!");
            }
        } 
        catch (IOException ex) 
        {
            JOptionPane.showMessageDialog(rootPane,ex.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


